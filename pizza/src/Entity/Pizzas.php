<?php

namespace App\Entity;

use Doctrine\ORM\Mapping as ORM;
use Doctrine\Common\Collections\Collection;
use Doctrine\Common\Collections\ArrayCollection;

/**
 * @ORM\Entity(repositoryClass="App\Repository\PizzasRepository")
 */
class Pizzas
{
    /**
     * @ORM\Id()
     * @ORM\GeneratedValue()
     * @ORM\Column(type="integer")
     */
    private $id;

    /**
     * @ORM\Column(type="string", length=255)
     */
    private $nom;

    /**
     * @ORM\Column(type="text")
     */
    private $description;

    /**
     * @ORM\Column(type="float")
     */
    private $prix;

    /** 
     * @ORM\ManyToMany(targetEntity="Ingredients", inversedBy="pizzas")
     * @ORM\JoinTable(name="pizzas_ingredients") 
     */
    private $ingredients;

    public function getId()
    {
        return $this->id;
    }

    public function getNom(): ?string
    {
        return $this->nom;
    }

    public function setNom(string $nom): self
    {
        $this->nom = $nom;

        return $this;
    }

    public function getDescription(): ?string
    {
        return $this->description;
    }

    public function setDescription(string $description): self
    {
        $this->description = $description;

        return $this;
    }

    public function getPrix(): ?float
    {
        return $this->prix;
    }

    public function setPrix(float $prix): self
    {
        $this->prix = $prix;

        return $this;
    }

    // public function __construct()
    // {
    //     $this->ingredients = new ArrayCollection();
    // }

    public function getIngredients()
    {
        return $this->ingredients;
    }

    public function addIngredient(Ingredients $ingredient)
    {
        $this->ingredients[] =  $ingredient;
    }
}
